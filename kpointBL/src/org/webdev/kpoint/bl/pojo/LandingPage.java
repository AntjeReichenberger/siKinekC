package org.webdev.kpoint.bl.pojo;

public class LandingPage {

	private int landingPageId=-1;
	private String kpIdentifier;
	private String kpName;	
	private String prefix;
	private String location;
	private String stateProvName;
	private String nationality;
	private String metaTagKeyword;
	private String templateName;
	
	
	public int getLandingPageId() {
		return landingPageId;
	}
	public void setLandingPageId(int landingPageId) {
		this.landingPageId = landingPageId;
	}
	public String getKpIdentifier() {
		return kpIdentifier;
	}
	public void setKpIdentifier(String kpIdentifier) {
		this.kpIdentifier = kpIdentifier;
	}
	public String getKpName() {
		return kpName;
	}
	public void setKpName(String kpName) {
		this.kpName = kpName;
	}
	public String getPrefix() {
		return prefix;
	}
	public void setPrefix(String prefix) {
		this.prefix = prefix;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getStateProvName() {
		return stateProvName;
	}
	public void setStateProvName(String stateProvName) {
		this.stateProvName = stateProvName;
	}		
	public String getNationality() {
		return nationality;
	}
	public void setNationality(String nationality) {
		this.nationality = nationality;
	}
	public String getMetaTagKeyword() {
		return metaTagKeyword;
	}
	public void setMetaTagKeyword(String metaTagKeyword) {
		this.metaTagKeyword = metaTagKeyword;
	}
	public String getTemplateName() {
		return templateName;
	}
	public void setTemplateName(String templateName) {
		this.templateName = templateName;
	}
	
	
}
