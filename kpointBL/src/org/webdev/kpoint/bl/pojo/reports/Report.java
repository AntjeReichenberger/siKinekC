package org.webdev.kpoint.bl.pojo.reports;

public class Report
{
	private int reportId = -1;
	private String name;
	
	public int getReportId() {
		return reportId;
	}
	public void setReportId(int reportId) {
		this.reportId = reportId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
