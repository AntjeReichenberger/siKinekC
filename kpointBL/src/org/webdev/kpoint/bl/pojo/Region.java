package org.webdev.kpoint.bl.pojo;

public class Region {

	
	private int regionId = -1;
	private String name;
	
	public int getRegionId() {
		return regionId;
	}
	public void setRegionId(int regionId) {
		this.regionId = regionId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
